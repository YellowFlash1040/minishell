int	ft_atoi(const char *str)
{
	int	m;
	int	res;

	res = 0;
	m = 0;
	while (*str != '\0' && ((*str >= 9 && *str <= 13) || *str == 32))
		str++;
	if (*str == '-' || *str == '+')
		if (*str++ == '-')
			m = -1;
	while (*str >= '0' && *str <= '9')
		res = res * 10 + (*str++ - '0');
	if (m)
		res = -res;
	return (res);
}
